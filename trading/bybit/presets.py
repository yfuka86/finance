# -- ユーザー保存設定 -------------------------------------------------------

def _ensure_dir():
    PRESETS_DIR.mkdir(parents=True, exist_ok=True)


def save_config(name: str, config: dict) -> Path:
    _ensure_dir()
    config["_saved_at"] = datetime.now().isoformat()
    config["_name"] = name
    path = PRESETS_DIR / f"{name}.json"
    path.write_text(json.dumps(config, indent=2, ensure_ascii=False))
    logger.info(f"設定を保存: {path}")
    return path


def load_config(name: str) -> dict:
    path = PRESETS_DIR / f"{name}.json"
    if not path.exists():
        raise FileNotFoundError(f"設定が見つかりません: {path}")
    return json.loads(path.read_text())


def list_saved_configs() -> list[dict]:
    _ensure_dir()
    configs = []
    for p in sorted(PRESETS_DIR.glob("*.json")):
        try:
            data = json.loads(p.read_text())
            configs.append({
                "name": data.get("_name", p.stem),
                "file": p.name,
                "strategy": data.get("strategy", "?"),
                "symbol": data.get("symbol", "?"),
                "saved_at": data.get("_saved_at", "?"),
            })
        except Exception:
            pass
    return configs


def delete_config(name: str) -> bool:
    path = PRESETS_DIR / f"{name}.json"
    if path.exists():
        path.unlink()
        return True
    return False


def get_presets_for_strategy(strategy: str) -> dict:
    return {k: v for k, v in STRATEGY_PRESETS.items() if v["strategy"] == strategy}


